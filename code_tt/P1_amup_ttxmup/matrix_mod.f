      CALL FFV2_4P0_3(W(1,5),W(1,2),GC_68,GC_77,MDL_MZ,MDL_WZ,W(1,10))
C     Amplitude(s) for diagram number 4
      CALL FFV2_5_0(W(1,4),W(1,6),W(1,10),GC_69,GC_76,AMP(4))
      CALL FFV1_2(W(1,4),W(1,1),GC_2,MDL_MT,MDL_WT,W(1,6))
