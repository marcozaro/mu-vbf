      CALL FFV1_1(W(1,2),W(1,1),GC_3,ZERO,ZERO,W(1,6))
C     Amplitude(s) for diagram number 7
      CALL FFV1_0(W(1,5),W(1,6),W(1,7),GC_3,AMP(7))
C     Amplitude(s) for diagram number 8
      CALL FFV2_4_0(W(1,5),W(1,6),W(1,8),GC_68,GC_77,AMP(8))
C     JAMPs contributing to orders QCD=2 QED=1
